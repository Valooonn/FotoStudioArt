let currentLanguage = "en";
let currentTheme = "light";
let currentPortfolioCategory = "all";
let currentTemplateCategory = "wedding";

document.addEventListener("DOMContentLoaded", () => {
  document.body.classList.add("is-loaded");

  setupNavigation();
  setupLanguageSwitcher();
  setupThemeToggle();
  setupSmoothScroll();
  applyLanguage("en");
  setupRevealObserver();
});

function getText(key) {
  return translations[currentLanguage][key] || translations.en[key] || key;
}

function getGroupText(group, key) {
  return translations[currentLanguage][group]?.[key] || translations.en[group]?.[key] || key;
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "en";
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = getText(key);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
    button.setAttribute("title", languageNames[button.dataset.lang]);
  });

  updateThemeButton();
  renderPortfolio(currentPortfolioCategory);
  renderServices();
  renderOffers();
  renderInvitationServices();
  renderTemplateFilters();
  renderTemplates(currentTemplateCategory);
  setupRevealObserver();
}

function setupLanguageSwitcher() {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.lang);
    });
  });
}

function setupThemeToggle() {
  const button = document.querySelector("[data-theme-toggle]");

  button.addEventListener("click", () => {
    currentTheme = currentTheme === "dark" ? "light" : "dark";
    document.body.classList.toggle("dark-mode", currentTheme === "dark");
    updateThemeButton();
  });

  document.body.classList.remove("dark-mode");
  currentTheme = "light";
  updateThemeButton();
}

function updateThemeButton() {
  const button = document.querySelector("[data-theme-toggle]");
  const label = document.querySelector("[data-theme-label]");
  const isDark = currentTheme === "dark";

  if (!button || !label) return;

  button.setAttribute("aria-pressed", String(isDark));
  button.setAttribute("aria-label", isDark ? getText("themeAriaLight") : getText("themeAriaDark"));
  label.textContent = isDark ? getText("lightMode") : getText("darkMode");
}

function setupNavigation() {
  const header = document.querySelector("[data-header]");
  const toggle = document.querySelector("[data-menu-toggle]");
  const nav = document.querySelector("[data-nav]");

  window.addEventListener("scroll", () => {
    header.classList.toggle("is-scrolled", window.scrollY > 24);
  });

  toggle.addEventListener("click", () => {
    const isOpen = document.body.classList.toggle("nav-open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      document.body.classList.remove("nav-open");
      toggle.setAttribute("aria-expanded", "false");
    });
  });
}

function setupSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach((link) => {
    link.addEventListener("click", (event) => {
      const target = document.querySelector(link.getAttribute("href"));

      if (!target) return;

      event.preventDefault();
      target.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  });
}

function setupRevealObserver() {
  const items = document.querySelectorAll(".reveal");

  if (!("IntersectionObserver" in window)) {
    items.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    { threshold: 0.16, rootMargin: "0px 0px -8% 0px" }
  );

  items.forEach((item) => observer.observe(item));
}

function renderPortfolio(activeCategory) {
  currentPortfolioCategory = activeCategory;
  const text = translations[currentLanguage];
  const filters = document.querySelector("[data-portfolio-filters]");
  const grid = document.querySelector("[data-portfolio-grid]");
  const categories = ["all", ...new Set(portfolioItems.map((item) => item.categoryKey))];

  filters.innerHTML = categories
    .map(
      (categoryKey) => `
        <button class="filter-button ${categoryKey === activeCategory ? "is-active" : ""}" type="button" data-category="${categoryKey}">
          ${text.categories[categoryKey]}
        </button>
      `
    )
    .join("");

  const filteredItems =
    activeCategory === "all"
      ? portfolioItems
      : portfolioItems.filter((item) => item.categoryKey === activeCategory);

  grid.innerHTML = filteredItems
    .map((item) => {
      const itemText = text.portfolio[item.key];
      return `
        <article class="portfolio-card reveal">
          <img src="${item.image}" alt="${itemText.title}" loading="lazy" />
          <div class="portfolio-card-content">
            <span>${text.categories[item.categoryKey]}</span>
            <h3>${itemText.title}</h3>
            <p>${itemText.text}</p>
          </div>
        </article>
      `;
    })
    .join("");

  filters.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => renderPortfolio(button.dataset.category));
  });
}

function renderServices() {
  const grid = document.querySelector("[data-services-grid]");

  grid.innerHTML = serviceKeys
    .map((serviceKey, index) => {
      const service = getGroupText("services", serviceKey);
      const initials = service
        .split(" ")
        .filter(Boolean)
        .slice(0, 2)
        .map((word) => word[0])
        .join("")
        .replace("/", "");

      return `
        <article class="service-card reveal">
          <div class="service-icon">${initials}</div>
          <h3>${service}</h3>
          <span>${getText("serviceLabel")} ${String(index + 1).padStart(2, "0")}</span>
          <p>${getText("serviceAvailable")}</p>
        </article>
      `;
    })
    .join("");
}

